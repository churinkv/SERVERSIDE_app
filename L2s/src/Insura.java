public class Insura {
    protected int range;
    private String client;
    private InsuraType type;

    public int getRange() {
        return range;
    }

    public void setRange(int range) {
        this.range = range;
    }

    public String getClient() {
        return client;
    }

    public void setClient(String client) {
        this.client = client;
    }

    public InsuraType getType() {
        return type;
    }

    public void setType(InsuraType type) {
        this.type = type;
    }
}
